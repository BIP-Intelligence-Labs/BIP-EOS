from pathlib import Path

ROOT = Path.cwd()

STRUCTURE = {
    "src/bip_eos/package_manager": [
        "__init__.py","engine.py","manifest.py","repository.py","dependency.py",
        "installer.py","validator.py","updater.py","verifier.py",
        "rollback.py","transaction.py","report.py","package.py","cli.py"
    ],
    "src/bip_eos/package_manager/services": [
        "__init__.py","resolver.py","installer_service.py",
        "registry_service.py","graph_service.py","manifest_service.py"
    ],
    "src/bip_eos/package_manager/governance": [
        "__init__.py","policies.py","compliance.py","standards.py"
    ],
    "src/bip_eos/package_manager/tests": [
        "__init__.py","test_engine.py","test_dependency.py",
        "test_installer.py","test_manifest.py"
    ],
    "src/bip_eos/package_manager/manifests": [
        "runtime.toml","graph.toml","registry.toml",
        "compiler.toml","migration.toml","ai.toml"
    ],
}

PY_HEADER='''"""
UEPM-001 Component
Auto-generated by UEOS Bootstrap.
"""\n'''

TOML='''name = "{name}"
version = "0.1.0"
entry = "bip_eos.{name}.engine"
'''

def create(path):
    if path.exists():
        print(f"[EXISTS ] {path}")
        return
    if path.suffix==".toml":
        path.write_text(TOML.format(name=path.stem),encoding="utf-8")
    else:
        path.write_text(PY_HEADER,encoding="utf-8")
    print(f"[CREATE ] {path}")

print("="*72)
print("UEPM-001 Package Manager Bootstrap")
print("="*72)

for folder, files in STRUCTURE.items():
    d=ROOT/folder
    d.mkdir(parents=True,exist_ok=True)
    print(f"[ OK ] {folder}")
    for f in files:
        create(d/f)

print("="*72)
print("UEPM-001 scaffold complete.")
