ensure_dir(EKS)

for rel, files in TREE.items():
    d = EKS / rel
    ensure_dir(d)
    readme = d / "README.md"
    if not readme.exists() and files:
        ensure_file(readme)
    for file in files:
        ensure_file(d / file)

