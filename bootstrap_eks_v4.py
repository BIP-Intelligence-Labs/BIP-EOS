HEADER = "# BIP Engineering Labs\n\nGenerated by Bootstrap Intelligence Platform.\n"

def ensure_dir(d):
    if d.exists():
        print(f"[SKIP] {d}")
    else:
        d.mkdir(parents=True, exist_ok=True)
        print(f"[DIR ] {d}")

def ensure_file(f, content=HEADER):
    if f.exists():
        print(f"[SKIP] {f}")
    else:
        f.write_text(content, encoding="utf-8")
        print(f"[FILE] {f}")

